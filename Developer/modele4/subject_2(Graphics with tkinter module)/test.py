        self.score += 1
        # –ø–∏—à–µ–º –Ω–æ–≤–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ —Å—á—ë—Ç–∞
        self.canvas.itemconfig(self.id, text=self.score)
