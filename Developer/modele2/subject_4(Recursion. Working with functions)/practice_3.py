'''
Напишите программу, которая принимает на вход строку и букву. Надо определить(с использованием рекурсии), сколько раз данная буква встречается в строке. 
Пример ввода:
Введите строку: hello world
Введите букву для проверки: l
Вывод:
Количество вхождений:
3
'''
def check(string, buckva):
    if not string:
        return 0
    elif string[0] == buckva:
        return 1 + check(string[1:], buckva)
    else:
        return check(string[1:], buckva)

string = input('Введите строку: ')
buckva = input('Введите букву которую будем искать: ')
print('Количество вхождений: ', check(string, buckva))
