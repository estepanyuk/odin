#Задача 1
'''
Напишите программу, которая принимает на вход число и рекурсивно определяет, четное это число или нечетное.
Пример ввода:
Введите число: 126
Вывод:
Число четное!

Пример ввода:
Введите число: 1253
Вывод:
Число нечетное!
'''
def num(x):
    if x < 2:
        return x % 2 == 0
    return num(x-2)

check = int(input('Введите целое число: '))
print('Число', 'четное' if num(check) else 'нечетное')

#задача 2
'''
Напишите программу, которая принимает на вход два числа и при помощи рекурсии находит их произведение.
Пример ввода:
Введите первое число: 15
Введите второе число: 10
Вывод:
Произведением двух чисел будет: 150
'''
def prod(a, b):
    if a < b:
        return prod(b, a)
    elif b != 0:
        return a + prod(a, b - 1)
    else:
        return 0

x = int(input('Введите целое число: '))
y = int(input('Введите целое число: '))
print('Произведение двух чисел: ', prod(x,y))


